package com.chen.bean;

public class Cost {
	private double jjprice;
	private double csprice;
	private double hfprice;
	public double getJjprice() {
		return jjprice;
	}
	public void setJjprice(double jjprice) {
		this.jjprice = jjprice;
	}
	public double getCsprice() {
		return csprice;
	}
	public void setCsprice(double csprice) {
		this.csprice = csprice;
	}
	public double getHfprice() {
		return hfprice;
	}
	public void setHfprice(double hfprice) {
		this.hfprice = hfprice;
	}
}
